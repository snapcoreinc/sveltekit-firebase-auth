<h1>This is the index page</h1>

This project is an example to show Firebase auth used on the client and accessed via a Svelte Store.

<ul>
	<li>Client-Server sync of session via a HttpOnly cookie</li>
	<li>Demonstrates using Firebase auth as an IAM</li>
	<li>Authentication using Google & Email</li>
	<li>Asynchronous loading of the profile</li>
	<li>Lazy loading of Firebase 9.0 SDK</li>
	<li>Proper handling of authentication when linking to a protected pages</li>
	<li>Use of the Firebase emulator for local testing</li>
	<li>Should be trivial to add new authentication providers in Firebase</li>
</ul>
